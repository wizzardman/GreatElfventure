<h3>Winter Year 1 Results</h3>
<p><strong>Results-</strong></p>
<p><strong>Economic- </strong>Turning a barren, toxic, flame-scarred wasteland into a fertile garden is no small task, even for someone like the Hunger. Even he, along with your increasingly more bold civil planning committee, aren&rsquo;t able to pull it off in a scant few months. The plans to compost the city&rsquo;s waste with ash doesn&rsquo;t pan out, and the first few test gardens fail within a few weeks. After some time the Hunger is able to determine that there are trace amounts of alchemical substances in the ash that are extremely toxic, even in small amounts. The Hunger has the werewithal to realize that this is also the case in the water filtration system, and he is able to determine how to purify the ash before anybody in the city starts getting sick.</p>
<p>While the attempts to build the hanging gardens don&rsquo;t succeed this turn, the civil planning committee&rsquo;s momentum is not disrupted. Progress is made, and plans and processes are improved. You <em>do not </em>lose the upgrade this time, and will get it again the next time you attempt to improve the Aqua Via. Plans have also improved enough that the next attempt will also reduce difficulty by one.</p>
<p>In addition, while the caverns aren&rsquo;t quite cool enough, the committee believes if a small amount of water from the Via in the mountains is diverted it may cool the caves enough to safely store livestock or other heat sensitive material. This will happen automatically the next time you improve the Via (unless you want to make it an Aspect itself.)</p>
<p><strong>Political-</strong> Your soldiers return successful, having gotten a full report on the Blade elves without being detected. They had to adjust tactics, as it seems that the Blade elves are still using military scouts to search for spies. One of your soldiers went plainclothes and snuck in with a group of farmers entering the fort with their food shipments. Once inside, security was&hellip; well, lax isn&rsquo;t the right word but as long as he kept his head down he was able to get a good look at what all was going on.</p>
