<p>It seems that the Blade elves have been busy strengthening the fort and securing their position. While the city&rsquo;s garrison is full, the simple barracks are being expanded into some kind of larger facility. Your spy is not sure what that means.</p>
<p>Your spy also reports something unusual. As he was finishing up his observations, he heard an alarm among the guards. As he fled, he heard the guards yelling about an intruder. Once he managed to flee the fort, he became aware that the guards were searching the opposite side of the city from him. He was also the only one of your spies in the city at the time. Whoever the blade elves found, it wasn&rsquo;t one of yours.</p>
<p><strong>Military- </strong>Congratulations! You have created a new unit of level 3 Ashkin, lumped in with the currently existing Division. The political side of the recruitment is a bit more complicated. The Terror favors the most dedicated, outspoken, and angry - who also just happen to be some of the biggest Ashkin supporters. There is significant whinging about &ldquo;forced conscription&rdquo; and whatnot, but military discipline keeps most in like. Military punishment works on the rest.</p>
<p>While an actual military formation of Ashkin is technically a win for Tsun, this accomplishes several things. For starters, some of the most radical Ashkin supporters are now solidly enmeshed in a military command structure where it&rsquo;s far easier for the Triumvirate to keep an eye on them. Secondly, these Ashkin supporters are taken away from influential civilian jobs as they are now full time soldiers. Finally, it cements the idea that while they are an Ashkin oriented group, the Praesidium are firmly under Triumvirate control.</p>
<p>Despite their initial complaints, this new unit is quite enthusiastic to get started. Any actions involving this unit or the Praesidium next turn will get a bonus die.</p>
